class Media {
    constructor({
        geometry, gl, image, index, length, renderer, scene, screen, text, viewport, bend, textColor, borderRadius = 0, font
    }) {
        this.extra = 0;
        this.geometry = geometry;
        this.gl = gl;
        this.image = image;
        this.index = index;
        this.length = length;
        this.renderer = renderer;
        this.scene = scene;
        this.screen = screen;
        this.text = text;
        this.viewport = viewport;
        this.bend = bend;
        this.textColor = textColor;
        this.borderRadius = borderRadius;
        this.font = font;
        this.createShader();
        this.createMesh();
        this.createTitle();
        this.onResize();
    }
    createShader() {
        const texture = new Texture(this.gl, {
            generateMipmaps: true
        });
        this.program = new Program(this.gl, {
            depthTest: false,
            depthWrite: false,
            vertex: `
                precision highp float;
                attribute vec3 position;
                attribute vec2 uv;
                uniform mat4 modelViewMatrix;
                uniform mat4 projectionMatrix;
                uniform float uTime;
                uniform float uSpeed;
                uniform float uBend;
                varying vec2 vUv;
                void main() {
                    vUv = uv;
                    vec3 p = position;
                    // ÿ™ÿ£ÿ´Ÿäÿ± ÿßŸÑÿ™ŸÖŸàÿ¨ ÿßŸÑÿÆŸÅŸäŸÅ (Wave effect)
                    p.z = (sin(p.x * 4.0 + uTime * 0.5) * 0.3 + cos(p.y * 2.0 + uTime * 0.5) * 0.3) * (0.1 + abs(uSpeed) * 1.5);
                    gl_Position = projectionMatrix * modelViewMatrix * vec4(p, 1.0);
                }
            `,
            fragment: `
                precision highp float;
                uniform vec2 uImageSizes;
                uniform vec2 uPlaneSizes;
                uniform sampler2D tMap;
                uniform float uBorderRadius;
                uniform float uAlpha;
                varying vec2 vUv;
                
                // ÿØÿßŸÑÿ© ŸÖÿ≥ÿßŸÅÿ© ŸÑÿ≠ŸàÿßŸÅ ÿØÿßÿ¶ÿ±Ÿäÿ© (Rounded Box SDF)
                float roundedBoxSDF(vec2 p, vec2 b, float r) {
                    vec2 d = abs(p) - b;
                    return length(max(d, vec2(0.0))) + min(max(d.x, d.y), 0.0) - r;
                }
                
                void main() {
                    // ÿ≠ÿ≥ÿßÿ® ÿßŸÑŸÜÿ≥ÿ®ÿ© ŸàÿßŸÑÿ™Ÿàÿ≥Ÿäÿ∑ (Cover fit)
                    vec2 ratio = vec2(
                        min((uPlaneSizes.x / uPlaneSizes.y) / (uImageSizes.x / uImageSizes.y), 1.0),
                        min((uPlaneSizes.y / uPlaneSizes.x) / (uImageSizes.y / uImageSizes.x), 1.0)
                    );
                    vec2 uv = vec2(
                        vUv.x * ratio.x + (1.0 - ratio.x) * 0.5,
                        vUv.y * ratio.y + (1.0 - ratio.y) * 0.5
                    );
                    vec4 color = texture2D(tMap, uv);
                    
                    // ÿ™ÿ∑ÿ®ŸäŸÇ ÿßŸÑÿ≠ŸàÿßŸÅ ÿßŸÑÿØÿßÿ¶ÿ±Ÿäÿ©
                    // ÿ™ÿ≠ŸàŸäŸÑ vUv ŸÖŸÜ [0, 1] ÿ•ŸÑŸâ ŸÖŸàÿ∂ÿπ ŸÖÿ±ŸÉÿ≤Ÿä [ -0.5, 0.5 ]
                    vec2 centeredUv = vUv - 0.5;
                    // ÿ≠ÿ≥ÿßÿ® ŸÜÿµŸÅ ÿ≠ÿ¨ŸÖ ÿßŸÑÿ®ÿ∑ÿßŸÇÿ©
                    vec2 halfSize = vec2(0.5, 0.5);
                    // ÿ™ÿ≠ÿØŸäÿØ ÿ≠ÿ¨ŸÖ ÿßŸÑÿ≠ÿßŸÅÿ© ÿßŸÑÿØÿßÿ¶ÿ±Ÿäÿ© ÿ®ÿßŸÑŸÜÿ≥ÿ®ÿ© ŸÑÿ≠ÿ¨ŸÖ ÿßŸÑÿ®ÿ∑ÿßŸÇÿ©
                    float radius = uBorderRadius * halfSize.y; 
                    
                    float d = roundedBoxSDF(centeredUv, halfSize - radius, radius);
                    
                    // ÿ™ŸÜÿπŸäŸÖ ÿßŸÑÿ≠ŸàÿßŸÅ (Antialiasing)
                    float edgeSmooth = 0.002;
                    float alpha = 1.0 - smoothstep(-edgeSmooth, edgeSmooth, d);
                    
                    gl_FragColor = vec4(color.rgb, color.a * alpha);
                }
            `,
            uniforms: {
                tMap: { value: texture },
                uPlaneSizes: { value: [0, 0] },
                uImageSizes: { value: [0, 0] },
                uSpeed: { value: 0 },
                uTime: { value: 100 * Math.random() },
                uBorderRadius: { value: this.borderRadius },
                uAlpha: { value: 1 }
            },
            transparent: true
        });
        const img = new Image();
        img.crossOrigin = 'anonymous';
        img.src = this.image;
        img.onload = () => {
            texture.image = img;
            this.program.uniforms.uImageSizes.value = [img.naturalWidth, img.naturalHeight];
        };
    }
    createMesh() {
        this.plane = new Mesh(this.gl, {
            geometry: this.geometry,
            program: this.program
        });
        this.plane.setParent(this.scene);
    }
    createTitle() {
        this.title = new Title({
            gl: this.gl,
            plane: this.plane,
            renderer: this.renderer,
            text: this.text,
            textColor: this.textColor,
            fontFamily: this.font
        });
    }
    update(scroll, direction) {
        // ÿ™ÿ≠ÿØŸäÿ´ ŸÖŸàÿ∂ÿπ ÿßŸÑÿ®ÿ∑ÿßŸÇÿ© ÿ®ŸÜÿßÿ°Ÿã ÿπŸÑŸâ ÿßŸÑÿ™ŸÖÿ±Ÿäÿ±
        this.plane.position.x = this.x - scroll.current - this.extra;

        const x = this.plane.position.x;
        const H = this.viewport.width / 2;

        // ÿ™ÿ∑ÿ®ŸäŸÇ ÿßŸÜÿ≠ŸÜÿßÿ° ÿßŸÑÿ£ŸÅŸÇ (Horizontal Bend)
        if (this.bend === 0) {
            this.plane.position.y = 0;
            this.plane.rotation.z = 0;
        } else {
            const B_abs = Math.abs(this.bend);
            const R = (H * H + B_abs * B_abs) / (2 * B_abs);
            const effectiveX = Math.min(Math.abs(x), H);

            const arc = R - Math.sqrt(R * R - effectiveX * effectiveX);
            if (this.bend > 0) {
                this.plane.position.y = -arc;
                this.plane.rotation.z = -Math.sign(x) * Math.asin(effectiveX / R);
            } else {
                this.plane.position.y = arc;
                this.plane.rotation.z = Math.sign(x) * Math.asin(effectiveX / R);
            }
        }

        // ÿ™ÿ≠ÿØŸäÿ´ ŸÖÿ™ÿ∫Ÿäÿ±ÿßÿ™ ÿßŸÑÿ¥ÿßÿØÿ± (Shaders)
        this.speed = scroll.current - scroll.last;
        this.program.uniforms.uTime.value += 0.04;
        this.program.uniforms.uSpeed.value = this.speed * 0.05; // ÿ™ŸÇŸÑŸäŸÑ ÿ™ÿ£ÿ´Ÿäÿ± ÿßŸÑÿ≥ÿ±ÿπÿ©

        // ÿ≠ŸÑŸÇÿ© ÿßŸÑÿ™ŸÉÿ±ÿßÿ± ÿßŸÑŸÑÿßŸÜŸáÿßÿ¶Ÿä (Infinite Loop)
        const planeOffset = this.plane.scale.x / 2;
        const viewportOffset = this.viewport.width / 2;
        this.isBefore = this.plane.position.x + planeOffset < -viewportOffset;
        this.isAfter = this.plane.position.x - planeOffset > viewportOffset;
        
        const loopWidth = this.widthTotal / 2; // ŸÜŸÇÿ≥ŸÖ ÿπŸÑŸâ 2 ŸÑÿ£ŸÜŸÜÿß ÿ∂ÿßÿπŸÅŸÜÿß ÿπÿØÿØ ÿßŸÑÿπŸÜÿßÿµÿ± ŸÖÿ≥ÿ®ŸÇŸãÿß
        
        if (direction === 'right' && this.isBefore) {
            this.extra -= loopWidth;
            this.isBefore = this.isAfter = false;
        }
        if (direction === 'left' && this.isAfter) {
            this.extra += loopWidth;
            this.isBefore = this.isAfter = false;
        }
    }
    onResize({ screen, viewport } = {}) {
        if (screen) this.screen = screen;
        if (viewport) {
            this.viewport = viewport;
        }
        
        // üõ†Ô∏è ÿ™ÿπÿØŸäŸÑ ŸÖŸÇÿßŸäŸäÿ≥ ÿßŸÑÿ®ÿ∑ÿßŸÇÿ© ŸÑÿ™ŸÉŸàŸÜ ŸÖÿ™ŸÜÿßÿ≥ÿ®ÿ© ŸÖÿπ ÿ¥ÿßÿ¥ÿßÿ™ ÿßŸÑÿ¨ŸàÿßŸÑ ÿ®ÿ¥ŸÉŸÑ ÿ£ŸÅÿ∂ŸÑ
        let scaleFactor = 1;
        if (this.screen.width < 768) { // ÿ¥ÿßÿ¥ÿßÿ™ ÿßŸÑÿ¨ŸàÿßŸÑ
            scaleFactor = 0.5;
        } else if (this.screen.width < 1024) { // ÿ¥ÿßÿ¥ÿßÿ™ ÿßŸÑÿ™ÿßÿ®ŸÑÿ™
            scaleFactor = 0.7;
        }
        
        const cardHeightScale = 800 * scaleFactor;
        const cardWidthScale = 600 * scaleFactor;
        
        // ÿ≠ÿ≥ÿßÿ® ŸÖŸÇÿßŸäŸäÿ≥ ÿßŸÑÿ®ÿ∑ÿßŸÇÿ©
        this.plane.scale.y = (this.viewport.height * cardHeightScale) / this.screen.height;
        this.plane.scale.x = (this.viewport.width * cardWidthScale) / this.screen.width;
        
        this.plane.program.uniforms.uPlaneSizes.value = [this.plane.scale.x, this.plane.scale.y];
        
        // üõ†Ô∏è ÿ™ŸÇŸÑŸäŸÑ ÿßŸÑŸÖÿ≥ÿßŸÅÿ© ÿ®ŸäŸÜ ÿßŸÑÿ®ÿ∑ÿßŸÇÿßÿ™
        this.padding = 1.0; 
        this.width = this.plane.scale.x + this.padding;
        
        // ÿßŸÑÿπÿ±ÿ∂ ÿßŸÑÿ•ÿ¨ŸÖÿßŸÑŸä ŸÑŸÖÿ¨ŸÖŸàÿπÿ© ÿßŸÑÿ®ÿ∑ÿßŸÇÿßÿ™ ÿßŸÑÿ£ÿµŸÑŸäÿ© (ÿ®ÿØŸàŸÜ ÿßŸÑÿ™ŸÉÿ±ÿßÿ±)
        this.widthTotal = this.width * (this.length / 2);
        
        // ŸÖŸàÿ∂ÿπ ÿßŸÑÿ®ÿ∑ÿßŸÇÿ© ŸÅŸä ÿßŸÑŸÖÿπÿ±ÿ∂ ÿßŸÑŸÑÿßŸÜŸáÿßÿ¶Ÿä
        this.x = this.width * this.index; 
        
        // ÿ•ÿπÿßÿØÿ© ÿ≠ÿ≥ÿßÿ® ŸÖŸÇÿßŸäŸäÿ≥ ÿßŸÑŸÜÿµ ÿ®ÿπÿØ ÿ™ÿ∫ŸäŸäÿ± ŸÖŸÇŸäÿßÿ≥ ÿßŸÑÿ®ÿ∑ÿßŸÇÿ©
        if (this.title) {
            const textHeight = this.plane.scale.y * 0.15;
            this.title.mesh.scale.y = textHeight;
            this.title.mesh.scale.x = textHeight * (this.title.width / this.title.height);
            this.title.mesh.position.y = -this.plane.scale.y * 0.5 - textHeight * 0.5 - 0.05;
        }
    }
}

